function openNav() {
    document.getElementById("side-nav").style.width = "93%";
    document.getElementById("side-nav").style.display = "flex";
}
function closeNav() {
    document.getElementById("side-nav").style.width = "0%";
    document.getElementById("side-nav").style.display = "none";
}
var imageTracker = 'black';
function espressoheart() {
    var image = document.getElementById('espressoheart');
    if(imageTracker == 'black'){
        image.src = 'heart-red.png';
        imageTracker = 'red';
    }
    else{
        image.src = 'heart.png';
        imageTracker = 'black';
    }
}
